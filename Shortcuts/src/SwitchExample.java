public class SwitchExample {
    public static void main(String[] args) {
        int value = 11;
        switch (value) {
            case 1:
                System.out.println("value = 1");
                break;
            case 10:
                System.out.println("value = 10");
                break;
            default:
                System.out.println("value N/A");
        }
    }
}
